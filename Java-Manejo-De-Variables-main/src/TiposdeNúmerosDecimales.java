public class TiposdeNúmerosDecimales {
    public static void main(String [] args){
        //Float en Java
        
        /*Tipo de dato float puede 
        almacenar números decimales medianos, 
        suficientes con un número decima de 
        6 o 7 dígitos, al final de numero 
        se coloca un f.*/
        
       float decimalMediano = 3.58f;
       System.out.println(decimalMediano);
        
        ///////////////////////////////////////
        
        //Double en Java
        
        /*Tipo de dato double puede almacenar 
        números decimales grandes, suficientes 
        con un número decima de 15 dígitos, al final 
        de numero se coloca un d aunque esto 
        es opcional, también puedes colocar 
        sin la d al final.*/
        double decimalGrande = 5.256d;
        decimalGrande = 5.4585;
        System.out.println(decimalGrande);
    }
}
