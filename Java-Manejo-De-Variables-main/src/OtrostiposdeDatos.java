public class OtrostiposdeDatos {
    public static void main(String [] args){
        //Char en Java
        
        /*Este tipo de dato es para 
        almacenar un carácter ose una 
        letra y también puede almacenar 
        código ASCII y como resultado 
        te devuelve el valor de ese código.*/
        
        char letra = 'a';
        char codigo = 65; //Resultado es igual -> A
        System.out.println(letra);
        System.out.println(codigo);
        
        ////////////////////////////////
        
        //Boolean en Java
        
        /*Este tipo de dato solo solo tiene 
        dos valores verdadero y falso (true y false), 
        estés datos se utilizan para realizar 
        condiciones u opciones lógicas.*/
        
        boolean verdad = true;
        boolean falso = false;

        System.out.println(verdad);
        System.out.println(falso);
        
        //String en Java
        
        /*String no es un tipo de dato 
        primitivo, este es un objeto 
        pero se comporta y se define como 
        un tipo de dato primitivo, este 
        tipo de dato es para almacenar 
        cadena de caracteres.*/
        
        String nombreCompleto = "Alex Roel ChB";
        System.out.println(nombreCompleto);
    }
}
